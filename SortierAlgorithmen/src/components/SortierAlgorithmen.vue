    insertionSort: function() {
      var iAktuellZufallsZahl = 0;
      var iVergleichszahl = 0;
      var iIndexZielArray = 0;
      var i = 1;

      this.iIndexQuellArray++;
      if(this.iIndexQuellArray >= this.iAnzahlZahlen) {
        alert("Ende der Sortierung erreicht!");
        return;
      }

      //Entferne die Markierung der letzten Sortierung
      if( this.iIndexZielArray >= 0) {
        this.arr_ziel[this.iIndexZielArray].style.backgroundColor = this.cUnsortiert;
      }
      else {
        this.arr_ziel[0].style.backgroundColor = this.cUnsortiert;
      }

      iAktuellZufallsZahl = parseInt(this.arr_quelle[this.iIndexQuellArray].innerHTML);
      iIndexZielArray = -1; // -1 da erst Inkrement und dann
      iVergleichszahl = -1;
      // Vergleiche die Zahl mit dem sortierten Array
      if(this.iIndexQuellArray > 0) {
        do {
          iIndexZielArray++;
          iVergleichszahl = parseInt(this.arr_ziel[iIndexZielArray].innerHTML);
        } while(iAktuellZufallsZahl > iVergleichszahl && iIndexZielArray < this.iIndexQuellArray - 1)
      }

      //Eintragen
      //iAktuellZufallsZahl ist die kleinste Zahl => vorne anfuegen
      if(iIndexZielArray == -1)  
      {
        iIndexZielArray = 0;
        this.arr_ziel[iIndexZielArray].innerHTML = iAktuellZufallsZahl.toString();
        this.arr_ziel[iIndexZielArray].style.backgroundColor = this.cSortiert;
        // merken fuer Zuruecksetzen
        this.iIndexZielArray = iIndexZielArray;
      }
      //iAktuellZufallsZahl ist die kleinste Zahl => vorne anfuegen
      else if(iIndexZielArray == 0 && iAktuellZufallsZahl < iVergleichszahl)
      {
        for(var j = this.iIndexQuellArray; j > iIndexZielArray; j--) {
          this.arr_ziel[j].innerHTML=this.arr_ziel[j-1].innerHTML;
          this.arr_ziel[j].style.backgroundColor=this.arr_ziel[j-1].style.backgroundColor;
        }
        this.arr_ziel[0].innerHTML = iAktuellZufallsZahl.toString();
        this.arr_ziel[iIndexZielArray].style.backgroundColor = this.cSortiert;
        // merken fuer Zuruecksetzen
        this.iIndexZielArray = iIndexZielArray;
      }
      //iAktuellZufallsZahl ist die groesste Zahl => hinten anfuegen
      else if(iIndexZielArray == this.iIndexQuellArray - 1 && iAktuellZufallsZahl > iVergleichszahl)
      {
        this.arr_ziel[iIndexZielArray+1].innerHTML=iAktuellZufallsZahl.toString();
        this.arr_ziel[iIndexZielArray+1].style.backgroundColor = this.cSortiert;
        // merken fuer Zuruecksetzen
        this.iIndexZielArray = iIndexZielArray+1;
      }
      //iAktuellZufallsZahl muss bei iIndexZielArray eingefuegt werden
      else
      {
        for(var j = this.iIndexQuellArray; j > iIndexZielArray; j--) {
          this.arr_ziel[j].innerHTML=this.arr_ziel[j-1].innerHTML;
          this.arr_ziel[j].style.backgroundColor=this.arr_ziel[j-1].style.backgroundColor;
        }
        this.arr_ziel[iIndexZielArray].innerHTML = iAktuellZufallsZahl.toString();
        this.arr_ziel[iIndexZielArray].style.backgroundColor = this.cSortiert;
        // merken fuer Zuruecksetzen
        this.iIndexZielArray = iIndexZielArray;
      } 

      //markiere die Elemente
      //1. Element? => kein Gruen
      if(this.iIndexQuellArray > 0)
      {
        this.arr_quelle[this.iIndexQuellArray-1].style.backgroundColor = this.cUnsortiert;
      }
      
      this.arr_quelle[this.iIndexQuellArray].style.backgroundColor = this.cSortiert;
      //letztes Element bereits erreicht? >= kein Gelb
      if(this.iIndexQuellArray+1 < this.iAnzahlZahlen)
      {
        this.arr_quelle[this.iIndexQuellArray+1].style.backgroundColor = this.cVergleich;
      }
