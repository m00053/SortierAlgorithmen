              document.getElementById("merge_gruppierung_" + (this.iRec+1).toString() + "_" + this.iPos[this.iRec].toString()).innerHTML = document.getElementById("merge_gruppierung_" + this.iRec.toString() + "_" + this.iVergleichRechts[this.iRec].toString()).innerHTML;
