    zufall: function (event) {
      //Erzeuge die Zufallszahlen

      //lokale Zufalls-Variable fuer das Erstellen einer zufaelligen Zahlenreihe (zum Sortieren)
      var iRnd = 0;
      //lokales Werte-Array fuer das Erstellen einer zufaelligen Zahlenreihe (zum Sortieren)
      var arr_quellzahlenfolge = new Array(this.iAnzahlZahlen);
      //lokale Variable fuer die Anzahl verfuegbarer Zufallszahlen (keine Doppelungen)
      var iVerfuegbar = this.iAnzahlZahlen-1;

      //Quellzahlenfolge erstellen (hier zunaechst eine Reihe ganzer Zahlen)
      for (var i=0; i<this.iAnzahlZahlen; i++) {
        arr_quellzahlenfolge[i] = i + 1;
      }

      //Quellzahlenfolge durch Zufall (Math.random()) zufaellig ineinander vertauschen
      for (var i=0; i<this.iAnzahlZahlen; i++) {
        iRnd = parseInt(Math.random()*iVerfuegbar);  //0 bis 29
        this.arr_quelle[i].innerHTML = arr_quellzahlenfolge[iRnd].toString();
        //trage dann zufaellige Zahlen aus der Reihe im Ziel ein

        // Zahl aus dem Array herausnehmen und von rechts "zusammenschieben"
        for (var j=iRnd; j<iVerfuegbar; j++) {
          arr_quellzahlenfolge[j] = arr_quellzahlenfolge[j+1];
        }
        //Je Vorgang eine Zahl weniger verfuegbar
        iVerfuegbar--;
      }

      //Initialisieren der Steuerlemente (Buttons) der Ziel-Zahlenreihe (zunaechst "leer")
      for(var i=0;i<this.arr_ziel.length;i++) {
        this.arr_ziel[i].innerHTML = "";
      }

      //Zahlenreihe fuer ""'Neu' setzen" einspeichern (entsprechend der aktuellen Anzahl an Zahlen)
      for (var i=0;i<this.iAnzahlZahlen;i++) {
        document.getElementById("merge_gruppierung_1_" + (i+1).toString()).innerHTML = this.arr_quelle[i].innerHTML;
      }

      //Zuruecksetzen des Start-Index des Quell-Werte-Arrays
      this.iIndexQuellArray = -1;
    },

    neu: function(event) {
      //Haupt-Initialisierung durchfuehren
      this.init();
    },

    weiter: function(event) {
      if (this.bFertig){
        alert("Die Aufgabe ist bereits sortiert!")
      } else {
        if(this.aktuellerAlgorithmus == "Insertion-Sort"){
          this.insertionSort();
        } else if(this.aktuellerAlgorithmus == "Selection-Sort")
        {
          this.selectionSort();
        } else if(this.aktuellerAlgorithmus == "Merge-Sort")
        {
          this.mergeSort();
        } else {
          alert("Kein Algorithmus gewaehlt!");
        }
      }
    }
  },

