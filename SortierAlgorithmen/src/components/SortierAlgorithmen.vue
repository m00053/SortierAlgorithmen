    selectionSort: function(){
      var temp = 0;
      if(this.bInit == false){
        this.bInit = true;
        for(var i=0; i<this.iAnzahlZahlen;i++){
          this.arr_ziel[i].innerHTML = this.arr_quelle[i].innerHTML;
        }
        this.indexAktuelleZahl = 0;
        //nimm die erste Zahl als kleinste Zahl an
        this.indexKleinsteZahl = this.indexAktuelleZahl;
        //nimm die erste Zahl als kleinste Zahl an
        this.indexVergleichsZahl = this.indexAktuelleZahl + 1;
        this.arr_ziel[this.indexKleinsteZahl].style.backgroundColor = this.cKleinsteZahl;
        this.arr_ziel[this.indexVergleichsZahl].style.backgroundColor = this.cVergleich;
        return;
      }

      //die letzte Zahl ist fuer sich selbst immer sortiert, daher -1
      if(this.indexAktuelleZahl<this.iAnzahlZahlen-1){
        //ansonsten ueberpruefe fuer jede weitere entlang des Arrays
        if(this.indexVergleichsZahl<this.iAnzahlZahlen){
            //ob es eine kleinere gibt.
          if(parseInt(this.arr_ziel[this.indexVergleichsZahl].innerHTML) < parseInt(this.arr_ziel[this.indexKleinsteZahl].innerHTML)){
            this.arr_ziel[this.indexKleinsteZahl].style.backgroundColor = this.cUnsortiert;
            //Wenn ja, bestimme diese als neue kleinste Zahl
            this.indexKleinsteZahl = this.indexVergleichsZahl;
          }

          this.arr_ziel[this.indexVergleichsZahl].style.backgroundColor = this.cUnsortiert;
          //gehe zur naechsten Zahl
          this.indexVergleichsZahl++;
          if(this.indexVergleichsZahl<this.iAnzahlZahlen){
            this.arr_ziel[this.indexVergleichsZahl].style.backgroundColor = this.cVergleich;
          }
          this.arr_ziel[this.indexKleinsteZahl].style.backgroundColor = this.cKleinsteZahl;
        } 

        // j hat das Ende des Arrays erreicht, trage Ergenis ein
        if(this.indexVergleichsZahl>=this.iAnzahlZahlen) {
          //Dann tausche die beiden Zahlen im Array
          temp = this.arr_ziel[this.indexKleinsteZahl].innerHTML;
          //Ring-Tausch (Puffer-Variable als Zwischenspeicher)
          this.arr_ziel[this.indexKleinsteZahl].innerHTML = this.arr_ziel[this.indexAktuelleZahl].innerHTML;
          this.arr_ziel[this.indexAktuelleZahl].innerHTML = temp;

          this.arr_ziel[this.indexKleinsteZahl].style.backgroundColor = this.cUnsortiert;
          this.arr_ziel[this.indexAktuelleZahl].style.backgroundColor = this.cSortiert;
          
          // erhoehe this.indexAktuelleZahl
          this.indexAktuelleZahl++;
          //nimm die erste Zahl als kleinste Zahl an
          this.indexKleinsteZahl = this.indexAktuelleZahl;
          //nimm die erste Zahl als kleinste Zahl an
          this.indexVergleichsZahl = this.indexAktuelleZahl + 1;
          if(this.indexAktuelleZahl<this.iAnzahlZahlen-1){
            this.arr_ziel[this.indexKleinsteZahl].style.backgroundColor = this.cKleinsteZahl;
            this.arr_ziel[this.indexVergleichsZahl].style.backgroundColor = this.cVergleich;
          }
          else
          {
            this.arr_ziel[this.indexAktuelleZahl].style.backgroundColor = this.cSortiert;
            alert("Selection-Sort beendet!");
          }
        }
      } else {
        this.arr_ziel[this.indexAktuelleZahl].style.backgroundColor = this.cSortiert;
        alert("Selection-Sort beendet!");
      }
    },

